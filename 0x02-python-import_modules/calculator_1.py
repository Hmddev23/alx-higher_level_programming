#!/usr/bin/python3

def add(a, b):
    """Addition function

    Args:
        a: first parameter
        b: second parameter

    Returns:
        The sum value of a + b
    """
return (a + b)


def sub(a, b):
    """Subtraction function

    Args:
        a: first parameter
        b: second parameter

    Returns:
        The sub value of a - b
    """
return (a - b)


def mul(a, b):
    """Multiplication function

    Args:
        a: first parameter
        b: second parameter

    Returns:
        The mul value of a * b
    """
return (a * b)


def div(a, b):
    """Division function

    Args:
        a: first parameter
        b: second parameter

    Returns:
        The div value of a / b
    """
return int(a / b)
